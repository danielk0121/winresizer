def 창_위치_계산(화면_크기, 모드):
    """
    주어진 화면 크기와 모드에 따라 창의 위치와 크기(x, y, w, h)를 반환합니다.
    """
    화면_너비, 화면_높이 = 화면_크기

    if 모드 == "좌측_절반":
        return (0, 0, 화면_너비 // 2, 화면_높이)
    
    if 모드 == "우측_절반":
        return (화면_너비 // 2, 0, 화면_너비 // 2, 화면_높이)
    
    if 모드 == "중앙_고정":
        # 1200x800 해상도 중앙 배치 (기본값)
        창_너비, 창_높이 = 1200, 800
        x = (화면_너비 - 창_너비) // 2
        y = (화면_높이 - 창_높이) // 2
        return (x, y, 창_너비, 창_높이)
    
    return (0, 0, 화면_너비, 화면_높이)
